package com.javadiary;

public class Operators {

	public static void main(String[] args) {
		int ten = 10;
		int addition = ten + ten;
		int subtraction = ten - ten;
		int division = ten / ten;
		int mutiplication = ten * ten;
		int modulus = ten % ten;
		System.out.println("Below is the example of Java Operators");
		System.out.println("10+10=" + addition);
		System.out.println("10-10=" + subtraction);
		System.out.println("10*10=" + mutiplication);
		System.out.println("10/10=" + division);
		System.out.println("10%10=" + modulus);

	}

}
